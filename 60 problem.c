// Printing number in a row...?


#include<stdio.h>
int main()
{
    int n=1;

    printf("%d\n", n);    //or we can use printf("%d\n", n++);
    n = n+1;
    printf("%d\n", n);
    n = n+1;
    printf("%d\n", n);
    n = n+1;
    printf("%d\n", n);
    n = n+1;
    printf("%d\n", n);
    n = n+1;
    return 0;

}
